const events = require("events");

const myEvents = new events();

////////////////////////////////////////////////////////////////

//TODO: // consume events ===> listen for events

// consuming events
myEvents.on("morning", (data) => {
  console.log(data);
});

// consuming events
myEvents.on("afternoon", (data) => {
  console.log(data);
});

////////////////////////////////////////////////////////////////////////////////////////////////

////        //////////////////////////////////////////////////////////
//TODO: // emit events =====> create events

/// emit() will help you in creating some events
myEvents.emit("morning", "Good Mornings guys");
myEvents.emit("afternoon", "Good Afternoon guys");

